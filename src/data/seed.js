function p(from, to, qualifier = 'biological') {
  return {
    id: `r_${from}_${to}`,
    from_person: from,
    to_person: to,
    type: 'parent',
    qualifier,
    partner_status: null,
  };
}

function partner(a, b, status) {
  return {
    id: `r_${a}_${b}`,
    from_person: a,
    to_person: b,
    type: 'partner',
    qualifier: 'biological',
    partner_status: status,
  };
}

